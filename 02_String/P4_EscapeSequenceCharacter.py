# New line (\n)
print("Example of New Line:")
print("Hello\nWorld")

print("\n-------------------\n")

# Tab space (\t)
print("Example of Tab Space:")
print("Hello\tWorld")

print("\n-------------------\n")

# Backslash (\\)
print("Example of Backslash:")
print("C:\\Users\\Gaurav")

print("\n-------------------\n")

# Single quote (\')
print("Example of Single Quote:")
print('It\'s a Python program')

print("\n-------------------\n")

# Double quote (\")
print("Example of Double Quote:")
print("He said \"Python is easy\"")

print("\n-------------------\n")

# Multiple escape sequences
print("Example with Multiple Escape Sequences:")
print("Name:\tGaurav\nCourse:\tPython Programming")